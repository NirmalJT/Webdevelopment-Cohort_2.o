// Define a value
let value = 7.8;
console.log("Original Value:", value);

// Rounds to the nearest integer
console.log("After round():", Math.round(value)); // 8

// Rounds up to the next integer
console.log("After ceil():", Math.ceil(value)); // 8

// Rounds down to the previous integer
console.log("After floor():", Math.floor(value)); // 7

// Generates a random number between 0 and 1
console.log("After random():", Math.random()); // Example: 0.4567

// Finds the maximum value from given numbers
console.log("After max():", Math.max(5, 10, 15)); // 15

// Finds the minimum value from given numbers
console.log("After min():", Math.min(5, 10, 15)); // 5

// Calculates power (value^2)
console.log("After pow():", Math.pow(value, 2)); // 60.84

// Calculates square root
console.log("After sqrt():", Math.sqrt(value)); // 2.79
