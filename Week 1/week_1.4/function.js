// A function in JavaScript is a set of statements that performs a task or
// calculates a value
// It should take some input and return an output where there is some obvious
// relationship between the input and the output.
////
//why we need function the first principle of programming is DRY(Don not repeat yourself)
//so when we write a funtion and call it we can obey the rule of DRY
function sum(a, b) {
  let result = a + b;
  return result;
}
console.log(sum(2, 2));
